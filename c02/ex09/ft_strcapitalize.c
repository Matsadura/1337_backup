char	*ft_strcapitalize(char *str)
{
	int	i;

	if (str[0] >= 'a' && str[0] <= 'z')
		str[0] -= 32;
	i = 1;
	while (str[i] != '\0')
	{
		if (str[i] == ' ' || str[i] == '-' || str[i] == '+')
		{
			if (str[i + 1] != '\0' && str[i + 1] >= 'a' && str[i + 1] <= 'z')
				str[i + 1] -= 32;
		}
		else
		{
			if (str[i] >= 'A' && str[i] <= 'Z'
				&& (str[i - 1] != ' '
					&& str[i - 1] != '-' && str[i - 1] != '+'))
				str[i] += 32;
		}
		i++;
	}
	return (str);
}
